// MARK: - 5. Job with database access

struct OrderConfirmationJob: PeregrineJob {
    struct Parameters: Codable {
        let orderID: UUID
    }

    func execute(parameters: Parameters, context: JobContext) async throws {
        guard let order = try await context.spectro.find(Order?.self, id: parameters.orderID) else {
            // Returning normally (without throwing) marks the job as discarded, not failed
            return
        }
        try await context.spectro.update(order, set: \.status, to: "confirmed")
        try await context.jobs.push(WelcomeEmailJob.self, parameters: .init(
            userID: order.userID,
            email:  order.userEmail
        ))
    }
}

