// JobsPatterns.swift
// Design specification for Peregrine.Jobs — inspired by Elixir's Oban
//
// Backed by swift-jobs (hummingbird-project) with Valkey or Postgres drivers.
// Peregrine adds a thin DSL, a generator, and dev-mode introspection.
//
// Driver selection:
//   .valkey(client:)    — fast, ephemeral (acceptable job loss on crash)
//   .postgres(client:)  — durable, transactional, survives restarts (recommended)

// MARK: - 1. App-level configuration

struct DonutShopApp: PeregrineApp {
    var jobs: JobQueue {
        if Peregrine.env == .test {
            return JobQueue.inMemory()
        }
        return JobQueue.postgres(spectro: spectro)  // reuses the Spectro connection pool
    }

    var routes: [Route] { [] }
}

// MARK: - 2. Define a job

// Generated by: peregrine gen job WelcomeEmail

struct WelcomeEmailJob: PeregrineJob {
    struct Parameters: Codable {
        let userID: UUID
        let email:  String
    }

    // Retry strategy: exponential back-off, up to 3 attempts
    static var retryStrategy: RetryStrategy { .exponentialJitter(maxAttempts: 3) }

    // Optional per-job timeout
    static var timeout: Duration { .seconds(30) }

    func execute(parameters: Parameters, context: JobContext) async throws {
        // context.spectro — access to the database
        let user = try await context.spectro.find(User.self, id: parameters.userID)
        try await Mailer.send(
            to:      parameters.email,
            subject: "Welcome to Donut Shop!",
            body:    "Hi \(user.name), your account is ready."
        )
    }
}

// MARK: - 3. Enqueue from a route handler

func registerUser(_ conn: Connection) async throws -> Connection {
    let user = try conn.decode(User.self)
    try await conn.spectro.insert(user)

    // Enqueue runs the job in the background; this request returns immediately
    try await conn.jobs.push(WelcomeEmailJob.self, parameters: .init(
        userID: user.id,
        email:  user.email
    ))

    return conn.redirect(to: "/dashboard")
}

